public class Queryone {
    public static void main(String[] args) {
        int value = 10;
        value = value + 1;
        System.out.println(value);

        value += 1;
        System.out.println(value);

        value++;
        System.out.println(value);

        value = value - 1;
        System.out.println(value);

        value -= 1;
        System.out.println(value);

        value--;
        System.out.println(value);
    }
}
